			--check south
			if (map[gridX][gridY] == 1 and gridY < gridMax) then			
				if (map[gridX][gridY + 1] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridY < gridMax) then			
				if (map[gridX][gridY + 1] == 1) then
					deadCount = deadCount + 1
				end
			end	
			--check west
			if (map[gridX][gridY] == 1 and gridX > 1) then		
				if (map[gridX - 1][gridY] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridX > 1) then			
				if (map[gridX - 1][gridY] == 1) then
					deadCount = deadCount + 1
				end
			end		
			--check ne
			if (map[gridX][gridY] == 1 and gridX < gridMax and gridY > 1) then			
				if (map[gridX + 1][gridY - 1] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridX < gridMax and gridY > 1) then			
				if (map[gridX + 1][gridY - 1] == 1) then
					deadCount = deadCount + 1
				end
			end		
			--check se
			if (map[gridX][gridY] == 1 and gridX < gridMax and gridY < gridMax) then		
				if (map[gridX + 1][gridY + 1] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridX < gridMax and gridY < gridMax) then			
				if (map[gridX + 1][gridY + 1] == 1) then
					deadCount = deadCount + 1
				end
			end		
			--check sw
			if (map[gridX][gridY] == 1 and gridX > 1 and gridY < gridMax) then			
				if (map[gridX - 1][gridY + 1] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridX > 1 and gridY < gridMax) then			
				if (map[gridX - 1][gridY + 1] == 1) then
					deadCount = deadCount + 1
				end
			end			
			--check nw
			if (map[gridX][gridY] == 1 and gridX > 1 and gridY > 1) then		
				if (map[gridX - 1][gridY - 1] == 1) then
					count = count + 1
				end
			elseif (map[gridX][gridY] == 0 and gridX > 1 and gridY > 1) then			
				if (map[gridX - 1][gridY - 1] == 1) then
					deadCount = deadCount + 1
				end
			end
			
			--check if cell lives or dies			
			if(map[gridX][gridY] == 1 and count < 2) then
				tempmap[gridX][gridY] = 0
			elseif (map[gridX][gridY] == 1 and count <= 3 and count > 1) then
				tempmap[gridX][gridY] = 1
			elseif (map[gridX][gridY] == 1 and count > 3) then
				tempmap[gridX][gridY] = 0
			--check if dead cell reanimates			
			elseif (map[gridX][gridY] == 0 and deadCount == 3) then
				tempmap[gridX][gridY] = 1
			elseif (map[gridX][gridY] == 0 and deadCount == 6 and replicatorMode == 1) then
				tempmap[gridX][gridY] = 1
			elseif (map[gridX][gridY] == 0 and deadCount ~= 3 and deadCount ~= 6) then
				tempmap[gridX][gridY] = 0			
			end
			
			if (gridX >= gridMax and gridY < gridMax) then
				gridX = 1
				gridY = gridY + 1
				count = 0
				deadCount = 0
			elseif (gridX >= gridMax and gridY >= gridMax) then
				gridX = 1
				gridY = 1
				count = 0
				deadCount = 0
				map = deepCopy(tempmap)
				generation = generation + 1
				tick = 0
			else
				gridX = gridX + 1
				count = 0
				deadCount = 0
			end
		end			
	end
end


function love.draw()

	--draw map
	for y=1, #map do
		for x=1, #map[y] do
			if map[y][x] == 1 then
				love.graphics.setColor(20, 248, 242)
				love.graphics.rectangle("fill", x * 16, y * 16, 16, 16)
			else
				love.graphics.rectangle("line", x * 16, y * 16, 16, 16)
			end
		end
	end
	
	--draw infotext
	love.graphics.setColor(20, 248, 242)
	love.graphics.print("gridX " .. gridX, 1000, 30)
	love.graphics.print("gridY " .. gridY, 1000, 45)
	love.graphics.print("gridMax " .. gridMax, 1000, 60)	
	love.graphics.print("generation " .. generation, 1000, 75)	
	love.graphics.print("tick " .. tick, 1000, 90)
	love.graphics.print("mouseX " .. mouseX, 1000, 105)
	love.graphics.print("mouseY " .. mouseY, 1000, 120)
	love.graphics.print("cells " .. numberOfCells, 1000, 135)
	love.graphics.print("Press r to randomize", 1000, 150)
	
end
 
function love.keypressed(key)
	if key == "escape" then
		love.event.quit()
	end	
	
	if key == "r" then
		do
			for gridY = 1, gridMax do
				for gridX = 1, gridMax do
					randomMap[gridX][gridY] = math.random(0, 1)
					if (gridX >= gridMax and gridY < gridMax) then
						gridX = 1
						gridY = gridY + 1
					elseif (gridX >= gridMax and gridY >= gridMax) then
						gridX = 1
						gridY = 1
						generation = 1
						map = deepCopy(randomMap)
					else
						gridX = gridX + 1
					end
				
				end
			end
		end
	end
end
 